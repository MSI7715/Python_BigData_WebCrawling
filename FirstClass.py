# 數字運算
print( 3 + 4 )      # 加法
print( 3 - 4 )      # 減法
print( 3 * 4 )      # 乘法
print( 3 / 4 )      # 除法
print( 3 ** 4 )     # 次方
print( 3 // 4 )     # 整除
print( 3 % 4 )      # 取餘數

